print("Note: The two numbers enered should be non zero integers")
num1 = int(input("Enter first number:"))
num2 = int(input("Enter second number:"))
print("\n Results")

#Addition
print(f"{num1}+{num2} = {num1 + num2}")
#Subtraction
print(f"{num1}-{num2} = {num1 - num2}")
#Multiplication
print(f"{num1}*{num2} = {num1 * num2}")
#Division
print(f"{num1}/{num2} = {num1 / num2}")
#Modulus
print(f"{num1}%{num2} = {num1 % num2}")
#Exponentiation
print(f"{num1}^{num2} = {num1 ** num2}")